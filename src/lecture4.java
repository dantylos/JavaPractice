public class lecture4 {
    public static void main(String[] args) {
        int[][] a1 = new int[3][3];

        for (int row = 0; row < a1.length; row++ ) {
            for (int col = 0; col < a1[row].length; col++ ) {
                System.out.print(a1[row][col] + " ");
            }
            System.out.println();
        }

        
    }
}
